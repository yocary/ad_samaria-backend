package com.ad_samaria.dto;

/**
 *
 * @author Yocary
 */
public class RolSistemaDTO {

    private Short id;
    private String nombre;

    public RolSistemaDTO() {
    }

    public RolSistemaDTO(Short id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    public Short getId() {
        return id;
    }

    public void setId(Short id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
